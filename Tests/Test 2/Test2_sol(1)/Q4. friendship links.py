def leastPopular(peopleDict):
    """
    Input: peopleDict is a dictionary of all people.
    Output: Return a set of least popular people.
    """
    leastPopular = [0]
    for person in peopleDict:
        if len(peopleDict[person]) < len(peopleDict[leastPopular[0]]):
            leastPopular = [person]
        elif len(peopleDict[person]) == len(peopleDict[leastPopular[0]]):
            leastPopular.append(person)
            
    return set(leastPopular)

def averagePopularity(peopleDict):
    """
    Input: peopleDict is a dictionary of all people.
    Output: Return the average number of friends that a person has.
    """
    total = 0
    for person in peopleDict:
        total += len(peopleDict[person])
    return total/len(peopleDict)

